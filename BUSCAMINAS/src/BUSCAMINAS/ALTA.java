        int ln=0;
        
  private void numeroLineas(String adress)
    {
        try
        {
            ln=1;
            RandomAccessFile raf = new RandomAccessFile(adress,"rw");
            for(int i=0; raf.readLine()!= null; i++)
            {
                ln++;
            }
            ln--;
        } catch (FileNotFoundException ex)
        {
            Logger.getLogger(DEFAULT.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DEFAULT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
    
        void setJugador(String name, String psw)
    {
        try {
            RandomAccessFile raf = new RandomAccessFile (f+"\\loginsJ.txt", "rw");
            for(int i = 0; i <ln;i++)
            {
                raf.readLine();
            }
            raf.writeBytes("\r\n");
            raf.writeBytes("\r\n");
            raf.writeBytes("Usuario:"+name+"\r\n");
            raf.writeBytes("Contrasena:"+psw+"\r\n");
            raf.writeBytes("Partidas Jugadas:0\r\n");
            raf.writeBytes("Partidas Ganadas:0\r\n");
            raf.writeBytes("Partidas Perdidas:0\r\n");
            raf.writeBytes("Minas a favor:0\r\n");
            raf.writeBytes("Minas en contra:0\r");
        } 
        catch (FileNotFoundException ex) 
        {
            Logger.getLogger(DEFAULT.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) 
        {
            Logger.getLogger(DEFAULT.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
